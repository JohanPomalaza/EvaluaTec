package com.example.evaluatec.modelos;

public class Estudiante {
    private int idUsuarioEstudiante;
    private String nombre;
    private String apellido;

    public int getIdUsuarioEstudiante() { return idUsuarioEstudiante; }
    public void setIdUsuarioEstudiante(int idUsuarioEstudiante) { this.idUsuarioEstudiante = idUsuarioEstudiante; }
    public String getNombre() { return nombre; }
    public void setNombre(String nombre) { this.nombre = nombre; }
    public String getApellido() { return apellido; }
    public void setApellido(String apellido) { this.apellido = apellido; }
}
